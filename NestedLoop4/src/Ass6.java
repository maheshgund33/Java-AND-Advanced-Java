
public class Ass6 {

	    public static void main(String[] args) {
	        int rows = 4; // Change this value to adjust the number of rows
	        

	        for (int i = 0; i < rows; i++) {
	        	int num=i+1;
	            for (int j = 0; j <= i; j++) {
	                System.out.print(num + " ");
	                num++;
	            }
	            System.out.println();
	        }
	    }
	


}
